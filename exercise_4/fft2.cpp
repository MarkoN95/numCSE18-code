void fft2(MatrixXcd &C, const MatrixXcd &Y) {

	Eigen::FFT<double> fft;

	MatrixXcd FnY(Y.cols(), Y.rows());

	for(int i = 0; i < Y.rows(); i++) {
		VectorXcd Y_row = Y.row(i);
		FnY.col(i) = fft.fwd(Y_row);
	}

	for(int i = 0; i < FnY.rows(); i++) {
		VectorXcd FnY_row = FnY.row(i);
		C.col(i) = fft.fwd(FnY_row);
	}
}

void ifft2(MatrixXcd &C, const MatrixXcd &Y) {
	fft2(C, Y.conjugate());
	C = C.conjugate() / (Y.rows() * Y.cols());
}

void conv2(MatrixXcd &C, const MatrixXcd &A1, const MatrixXcd &A2) {
	int m = A1.rows() + A2.rows() - 1;
	int n = A1.cols() + A2.cols() - 1;
	C.resize(m, n);

	MatrixXcd padded_A1 = MatrixXcd::Zero(m, n);
	padded_A1.block(0, 0, A1.rows(), A1.cols()) = A1;

	MatrixXcd padded_A2 = MatrixXcd::Zero(m, n);
	padded_A2.block(0, 0, A2.rows(), A2.cols()) = A2;

	MatrixXcd C1 = MatrixXcd::Zero(m, n);
	MatrixXcd C2 = MatrixXcd::Zero(m, n);
	fft2(C1, padded_A1);
	fft2(C2, padded_A2);
	ifft2(C, C1.cwiseProduct(C2));
}

